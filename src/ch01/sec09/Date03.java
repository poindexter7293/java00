package ch01.sec09;
/* 자바 프로그래밍 기초 문법 배우기

		 
✅ 자바 자료형?  자바에서 변수에 저장할 수 있는 값의 종류와 크기를 정해주는 데이터의 종류
		
		
✅ 자바 자료형 종류
		
| 구분      | 자료형 종류                                             | 설명                           
| ---------| -----------------------------------------------------|------------------------------ 
| 🧱 기본형 | byte, short, int, long, float, double, char, boolean  | 기본값을 저장하는 자료형 (숫자, 문자, 논리값 등) 
| 📦 참조형 | String, 배열, 클래스, 인터페이스 등                         | 객체나 주소를 저장하는 자료형             


🔹 1. 기본형 자료형 (Primitive Type)		
| 자료형      | 크기   | 저장하는 값              | 예시                               |
| --------- | ----- | -----------------------| -------------------------------- |
| `byte`    | 1바이트 | 작은 정수 (-128 \~ 127)  | `byte age = 20;`                 |
| `short`   | 2바이트 | 중간 크기 정수            | `short count = 30000;`           |
| `int`     | 4바이트 | 일반적인 정수             | `int price = 100000;`            |
| `long`    | 8바이트 | 큰 정수                 | `long population = 8000000000L;` |
| `float`   | 4바이트 | 실수 (소수점)            | `float pi = 3.14f;`              |
| `double`  | 8바이트 | 더 정밀한 실수            | `double rate = 3.141592;`        |
| `char`    | 2바이트 | 한 글자 (문자)           | `char grade = 'A';`              |
| `boolean` | 1바이트 | 참 또는 거짓             | `boolean isLogin = true;`        |

		
🔸 2. 참조형 자료형 (Reference Type)  <-- 일단 참조 자료형은 눈으로만 보고 넘어 가세요.

| 자료형      | 설명                               		| 예시                               |
| --------- | ------------------------------------------| ---------------------------------- |
| `String`  | 문자열을 저장하는 자료형                        | `String name = "Alice";`           |
| `배열`     | 같은 타입의 여러 값을 저장하는 자료형              | `int[] scores = {90, 80, 70};`     |
| `클래스`   | 사용자 정의 객체를 저장하는 자료형                 | `Person person = new Person();`    |
| `인터페이스` | 클래스가 구현해야 하는 메서드의 집합을 정의하는 자료형 | `Runnable runnable = new MyRunnable();`|
| `열거형`   | 상수들의 집합을 정의하는 자료형                    | `enum Color { RED, GREEN, BLUE }`  |


 ---------------------------------------------------------------------------------- 
		 
 ✅ 1. 변수란?
		변수는 값을 저장할 수 있는 '이름이 붙은 공간(그릇)'입니다.
		
		📌 쉽게 말하면:
		 - 변수를 만들면 → 컴퓨터 메모리에 공간이 생깁니다.
		 - 그 공간에 이름을 붙여 → 값을 저장하고, 나중에 그 값을 불러올 수 있게 됩니다.

 ✅ 2. 변수 선언이란?
		변수를 만든다고 '선언'하는 것을 말합니다.
		즉, 컴퓨터에게 '이런 이름으로 이런 값 저장할 공간을 주세요!' 라고 요청하는 겁니다.
	
	📌 자바에서 변수 선언 형식:     자료형 변수이름; 												
								 - 자료형: 어떤 종류의 값을 저장할 건지 알려줍니다. (예: 숫자, 문자, 논리 등)
								 - 변수이름: 그 공간에 붙일 이름입니다. (예: a, b, c 등)
 ✅ 3. 변수 선언 예시:
		- int a; // 정수형 변수 'a'를 선언합니다. (4바이트 크기의 정수를 저장할 공간)
		- double m; // 실수형 변수 'm'을 선언합니다. (8바이트 크기의 실수를 저장할 공간)
		- boolean k; // 논리형 변수 'k'를 선언합니다. (참 또는 거짓을 저장할 공간)	
		
 ✅ 4. 변수에 값 저장하기:  변수를 선언한 후에는 그 공간에 값을 저장할 수 있습니다.
		
		예를 들어, 
		a = 1; // 변수 'a'에 정수 1을 저장합니다.
		m = 2.4; // 변수 'm'에 실수 2.4를 저장합니다.
		k = true; // 변수 'k'에 논리값 true를 저장합니다.
		
 ✅ 5. 변수 사용하기:  변수에 저장된 값을 사용할 때는 변수 이름을 사용합니다.
		예를 들어, System.out.println(a); // 변수 'a'에 저장된 값을 출력합니다.
		
 ✅ 6. 변수의 값 변경하기: 변수에 저장된 값은 언제든지 변경할 수 있습니다.
		예를 들어, a = 2; // 변수 'a'에 새로운 값 2를 저장합니다. (이전 값 1은 사라집니다.)

 ✅ 7. 변수의 값 확인하기: 변수에 저장된 값을 확인하려면 System.out.println(변수이름);을 사용합니다.
		예를 들어, System.out.println(a); // 변수 'a'에 저장된 값을 출력합니다.
 */


