//예제  : 정수형 상수리터럴을 저장하는 변수 메모리 선언(만들기)
public class Date03 {

	public static void main(String[] args) {
		
		//  a=1; //error - 선언된 변수가 없어서 
		
		//자바에서는 변수메모리에 저장된 데이터를 꺼내와서 사용하기 위해서는 먼저 변수 메모리를 선언 해야 합니다.
		//변수 메모리는 반드시 특정 자료형으로 선언해야 합니다.
		
		//변수 메모리 생성(선언) 문법
		//자료형 변수명;
		
		//저장될 값의 종류에 따라 자료형을 결정 해야 하는데..
		//만약 1과 같은 정수를 저장하기 위해서는 다음과 같이 정수형 변수를 선언하면 됩니다.
		
		//4바이트 크기의 정수를 저장할 자료형 int로 a라는 이름의 변수 메모리 생성(a 변수 선언)
		int a;  // a변수 메모리 [ ] 를 선언하고
		
		a = 1;  // a변수 메모리 [ ]에 1 정수값을 저장
				// a변수 메모리 [1]
		
		//println(a); 구문으로 a변수 메모리 [1]에 저장된 1을 얻어 출력
		System.out.println(a);
		
		a = 2; //변수 메모리에 다른 값을 대입 하면 변수 메모리에 저장된 값은 제거 되고
			   //다른 값을 저장하여 값을 변경할 수 있다.
			   //1. a변수 메모리 [ 1 ]
			   //   a = 2;  <- a변수 메모리에 2를 저장
			   //2. a변수메모리 [ 2 ]  1은 제거 되고 마지막에 저장 시킨 2가 저장 됨 
		
		//마지막에 = 2 대입 한 값만 유지되는지 a변수에 저장된 값 불러와 출력
		System.out.println(a);
		
		double m;
		m = 24.4;
		
		boolean k = true;
	
		//byte b;
		short b;
		
		
		b = 128;
		
		int c = 32768;
		
		//int d = 123456L;
		long d = 123456L;
		
		
	}

}
