package ch01.sec09;

//예제 : 더 다양한 상수 출력

public class Date02 {

	public static void main(String[] args) {
		//(1) long 자료형 상수 : 숫자 끝에 L 혹은 l을 붙인 정수 
		System.out.println(1L);
		
		//(2) float 자료형 상수  : 숫자 끝에 F 혹은 f를 붙인 실수 
		System.out.println(1.5f);
		
		//(3) 문자열 :  이중 따옴표로 묶어준 데이터
		System.out.println("abc");

	}

}
